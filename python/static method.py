class aditya:
    @ staticmethod
    def add(x,y):
        print("the sum value is:",(x+y))
    @ staticmethod
    def sub(x,y):
        print("the sub value is:",(x-y))
    @ staticmethod
    def avg(x,y):
        print("the avg value is:",(x+y)/2)
aditya.add(200,100)
aditya.sub(200,100)
aditya.avg(200,100)
              
